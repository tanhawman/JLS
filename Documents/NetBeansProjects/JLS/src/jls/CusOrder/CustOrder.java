    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
         if (jCheckBox1.isSelected()){
        int rose = Integer.parseInt(jTextField2.getText()) * 2;
        String r = String.valueOf(rose);
        jLabel8.setText(r);
       }
       if (jCheckBox2.isSelected()){
       int Lily = Integer.parseInt(jTextField1.getText()) * 1;
       String l = String.valueOf(Lily);
        jLabel9.setText(l);
       }
      if (jCheckBox1.isSelected() && jCheckBox2.isSelected()){ 
       int total = (Integer.parseInt(jTextField2.getText())) * 2 + (Integer.parseInt(jTextField1.getText()) * 1);
       String t = String.valueOf(total);
       jLabel10.setText(t);
        } 
        else if (jCheckBox2.isSelected()){
       int total = 0 + (Integer.parseInt(jTextField1.getText()) * 1);
       String t = String.valueOf(total);
       jLabel10.setText(t);
        jLabel8.setText("0");
       }
        else if (jCheckBox1.isSelected() ){ 
      int total = (Integer.parseInt(jTextField2.getText())) * 2 + 0;
       String t = String.valueOf(total);
       jLabel10.setText(t);
        jLabel9.setText("0");
        }
        else{
            String t = String.valueOf(0);
       jLabel10.setText(t); 
        }
    }//GEN-LAST:event_jButton1MouseClicked

