    private void txtQtyKeyTyped(java.awt.event.KeyEvent evt) {                                
        char c = evt.getKeyChar();
        if(!(Character.isDigit(c) || (c == KeyEvent.VK_BACKSPACE) || c == KeyEvent.VK_DELETE)){
            evt.consume();
        }
    }
    
