    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String selectedRow;
        String selectedName;
        int nameEnd = 0;
        if(mpi.MPITable.getSelectedRow() != -1){
            selectedRow = mpi.MPITable.getValueAt(mpi.MPITable.getSelectedRow(), 0).toString();
            for (int i = 0; i < selectedRow.length(); i++) {
                if (selectedRow.charAt(i) == '(') {
                    nameEnd = i - 1;
                }
            }
            selectedName = selectedRow.substring(0, nameEnd);
            this.dispose();
            new ModifyProductInfo2(CustList, OrderList, ProductList, ArrangeList, selectedName).setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

